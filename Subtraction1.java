class Subtraction1{
	
	public static void main(String[] args){
		System.out.println(sub(12,23));
		System.out.println(sub(12,23));
		System.out.println(sub(126575,383,74));
		System.out.println(sub(11,35,97));
		System.out.println(sub(14,29,97));
		System.out.println(sub(15,93,97));
		System.out.println(sub(1653,673,97));
		System.out.println(sub(1234,256,97));
		System.out.println(sub(1234,53,97));
		System.out.println(sub(12344,43,95));
		
	}
	
	public static int sub(int a,int b){
		return a-b;
	}
	
	public static int sub(int a,int b, int c){
		return a-b-c;
	}
	
	
	
}